<h4>ГЕРМЕТИЧНОСТЬ</h4>
<p>Герметичность бронированных дверей зависит от качества сварной конструкции и их монтажа. Для обеспечения полной герметизации используется монтажная пена и резиновый уплотнитель. Когда двери плотно прилегают к раме, проблем с герметичностью не возникает.</p>
