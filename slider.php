<h4>ТЕПЛО- И ЗВУКОИЗОЛЯЦИЯ</h4>
<p>Уровень тепло- и звукоизоляции бронедверей зависит от материалов их изготовления. Благодаря использованию внутренних утеплителей (минеральной ваты Isover и Rockwool), которыми заполняется пустое пространство между ребрами жесткости, степень тепло- и звукоизоляции возрастает в разы. Отличную звукоизоляцию обеспечивает также декоративная панель, выполненная из звукопоглощающих материалов (панелей МДФ и массива дерева). Бронированные двери, изготовленные с применением изоляционных материалов отличаются высоким качеством, которое нельзя не заметить.</p>
