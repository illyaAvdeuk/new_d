<h3>Важный критерий качественной двери – надежный замок и его защита!</h3>
<p><img alt="" src="/wp-content/uploads/2014/03/lock.fw_.png" /></p>
<p class="white">Мы убеждены и хотим заверить вас: экономить на замке – себе дороже. Главное во входной бронедвери не внешний вид, а ее защитные свойства.</p>
<p>Замки &#8212; это наиболее уязвимая деталь бронированных дверей, поэтому ворам ничего не стоит взломать дешевый и некачественный замок. Чтобы уберечь себя от таких неприятностей, покупайте двери, оснащенные надежной замковой системой и специальными механизмами защиты от взлома (повышенным уровнем секретности). Возможность осуществления механического взлома с таким замком практически сводится к нулю. Каждая замковая система уникальна и имеет индивидуальные коды или ключи. Устойчивость к возможному взлому зависит от качества материалов, из которых выполнена дверь, и наличия приспособлений для усиления защиты. К примеру, на наружной части замковой системы может быть установлена броненакладка. Так называют элемент защиты цилиндрового механизма и собственно самого замка</p>
